#include <unistd.h>

int main(int ac , char **av)
{
	int i;
	int j;
	//int arr[256] = {0};
	
	if(ac == 3)
	{
		i = 0;
		j = 0;
		while(av[2][i] && av[1][j])
		{
				if(av[1][j] == av[2][i])
				{
					j++;
				}
					i++;
		}
		i = 0;
		while(av[1][i])
			i++;
		if(i == j)
		{
			write(1,av[1],j);
		}
	}
	write(1,"\n",1);	
}
