int	ft_atoi(const char *str)
{
	int i;
	int sign ;
	int nbr;

	i = 0;
	nbr = 0;
	sign = 1;

	while(str[i])
	{
		if(str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		{
			i++;
		}
		if(str[i] == '-' || str[i] == '+')
		{
			if(str[i] == '-')
				sign *= -1;
			i++;
		}
		if(str[i] >= 0 && str[i] <= 9)
		{
			nbr = (nbr * 10) + str[i] - 48;
			i++;
		}
	}
	return (nbr * sign);
}


