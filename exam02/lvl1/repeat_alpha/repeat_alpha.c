#include <unistd.h>
int main (int ac , char **av)
{
	int i;
	int c;

	if(ac == 2)
	{
		i = 0;
		while(av[1][i]){
			if (av[1][i] >= 'a' && av[1][i] <= 'z')
			{
				c = av[1][i];
				while(c >= 'a')
				{
					write(1,&av[1][i],1);
					c--;
				}	
			}
			if (av[1][i] >= 'A' && av[1][i] <= 'Z')
			{
				c = av[1][i];
				while (c >= 'A')
				{
					write(1,&av[1][i],1);
					c--;
				}
			}
			i++;
		}
	}
	write(1,"\n",1);
}
